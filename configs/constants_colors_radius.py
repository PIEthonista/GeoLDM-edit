def get_colors(atomic_symbol):
    return str(colors_radius_dic.get(atomic_symbol, "#FFFFFF_0.8").split("_")[0])

def get_radius(atomix_symbol):
    radius = colors_radius_dic.get(atomix_symbol, "#FFFFFF_0.8").split("_")[1]
    if radius == 'None':
        radius = 0.8
    return float(radius)